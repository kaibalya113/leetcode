package com.micro;

public class FirstMissingPositive {
    public static void main(String[] args){
        int[] nums = {1,2,0};
        System.out.println(missingNo(nums));
    }
    private static int missingNo(int[] nums){
        if(nums.length==0){
            return 0;
        }

        return 0;
    }
}
